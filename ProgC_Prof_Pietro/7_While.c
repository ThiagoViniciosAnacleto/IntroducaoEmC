#include <stdio.h>

int main()
{
	
	int i; // define i como numero inteiro
	
	i = 1; // seta o valor de i como 1 inicialmente
	
	while( i <= 10){  // enquanto o valor de i for menor ou igual a 10
		printf(  "%d \n", i ); // executa comando de exibição do valor de i
		i = i + 1; // executa a soma
	}

}
